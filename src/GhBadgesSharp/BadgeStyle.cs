namespace Grynwald.GhBadgesSharp
{
    /// <summary>
    /// Enumerates the supported styles of badges.
    /// </summary>
    public enum BadgeStyle
    {
        /// <summary>
        /// The "flat" badge style, ported from shields.io.
        /// See <see href="https://shields.io/#styles"/>.
        /// </summary>
        Flat,

        /// <summary>
        /// The "flat-square" badge style, ported from shields.io.
        /// See <see href="https://shields.io/#styles"/>.
        /// </summary>
        FlatSquare,

        /// <summary>
        /// The "plastic" badge style, ported from shields.io.
        /// See <see href="https://shields.io/#styles"/>.
        /// </summary>
        Plastic,

        /// <summary>
        /// The "for-the-badge" badge style, ported from shields.io.
        /// See <see href="https://shields.io/#styles"/>.
        /// </summary>
        ForTheBadge,

        /// <summary>
        /// The "social" badge style, ported from shields.io.
        /// See <see href="https://shields.io/#styles"/>.
        /// </summary>
        Social
    }
}
